	private String unfilter(String str, String path)
	{
		if (str.length() > 0) {
			path = path.replaceAll("(\\\\)", "\\\\\\\\\\\\\\\\");
			str = str.replaceAll("(\\\\)", "\\\\\\\\");
			str = str.replaceAll(path, "");
			return str.replaceAll("(\\\\\\\\)", "\\\\");
		}
		return str;
	}

	public String reformatText(String str)
	{
		if (str.length() > 20) {
			str = ".." + str.substring(str.length() - 20);
		}
		return str;
	}
