	private void jButton3ActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jButton3ActionPerformed
	{//GEN-HEADEREND:event_jButton3ActionPerformed
		//DEFAULT TILE
		String filePath = dialog.openFile("Default Tile", defaultTile.getParent(), "*.png");
		if (filePath != null) {
			File path = new File(".");
			File parent = libraryDirectory;
			File child = new File(filePath);
			try {
				path = path.getCanonicalFile();
				child = child.getCanonicalFile();
				parent = libraryDirectory.getCanonicalFile();
			} catch (Exception e) {
			}

			//Tile must be within the library
			if (isChildofFile(parent, child)) {
				defaultTile = new File(unfilter(filePath, path.getAbsolutePath() + "\\"));
			} else {
				JOptionPane.showMessageDialog(new Frame(), "The default tile must be a child of the library");
			}
		}
		jLabel3.setText(reformatText(defaultTile.toString()));
	}//GEN-LAST:event_jButton3ActionPerformed

	private boolean isChildofFile(File parent, File child)
	{
		if (child == null) {
			return false;
		}
		if (parent.equals(child)) {
			return true;
		}

		return isChildofFile(parent, child.getParentFile());
	}

