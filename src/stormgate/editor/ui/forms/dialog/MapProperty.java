	private void jButton4ActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jButton4ActionPerformed
	{//GEN-HEADEREND:event_jButton4ActionPerformed
		//SAVE
		File parent = libraryDirectory;
		File child = defaultTile;
		try {
			child = child.getCanonicalFile();
			parent = libraryDirectory.getCanonicalFile();
		} catch (Exception e) {
		}

		//Tile must be within the library
		if (isChildofFile(parent, child)) {
			Map map = data.getMap();
			map.setMapName(jTextField1.getText());
			//LibraryViewer.mainDirectory = libraryDirectory.toString();
			Tile.defaultTile = defaultTile.toString();
		} else {
			JOptionPane.showMessageDialog(new Frame(), "The default tile must be a child of the library");
			return;
		}

		data.refreshLibrary();
		data.refresh();
		this.setVisible(false);
	}//GEN-LAST:event_jButton4ActionPerformed

